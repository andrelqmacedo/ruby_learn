#By default Ruby will perform a floor division, meaning Ruby will always return an integer from normal division
puts 10 / 5  # => 2
puts 12 / 5 # => 2

puts 12 / 5.0 #Ruby will now give a floating point result

#Module operator will give us the remainder of the operation
puts 5 % 2


